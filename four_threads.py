import threading  #Thread Library
import time       #Time Library

s = ''    #Initializing Global variable as string

def shifttext(text, shift):   #Shifiting function
  a = ord('a')    #returns unicode of a
                #chr returns unicode string of one character
  return ''.join(chr((ord(char) - a + shift) % 26 + a) 
  for char in text.lower())   #converting to lower for ASCII code

